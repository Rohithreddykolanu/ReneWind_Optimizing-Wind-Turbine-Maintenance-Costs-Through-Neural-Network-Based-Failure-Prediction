# Define a function to compute classification performance metrics for a given model and threshold
def model_performance_evaluation(model, predictors, target, threshold = 0.5):

  """
  Function to compute different metrics to check classification model performance

  model: classifier
  predictors: independent variables
  target: dependent variable
  threshold: threshold for classifying the observation as class 1
  """

  # Generate predicted class labels by applying the threshold to the predicted probabilities
  y_pred = model.predict(predictors) > threshold

  # Compute accuracy, precision, recall, and F1 score (macro-averaged across both classes)
  accuracy = accuracy_score(target, y_pred)
  precision  = precision_score(target, y_pred, average = 'macro')
  recall = recall_score(target, y_pred, average = 'macro')
  f1 = f1_score(target, y_pred, average = 'macro')

  # Store the computed metrics in a single-row DataFrame and return it
  df_perf = pd.DataFrame({
      'Accuracy': accuracy,
      'Precision': precision,
      'Recall': recall,
      'F1 Score': f1},
      index = [0]
      )

  return df_perf

# Function to plot confusion matrix with count and percentage labels
def plot_confusion_matrix(model, predictors, target, threshold = 0.5):
    """
    To plot the confusion_matrix with percentages

    model: classifier
    predictors: independent variables
    target: dependent variable
    threshold: threshold for classifying the observation as class 1
    """

    # Predicting target values
    y_pred = model.predict(predictors) > threshold

    # Creating confusion matrix
    cm = metrics.confusion_matrix(target, y_pred)

    # Creating count and percentage labels
    labels = np.asarray(
        [
            ["{0:0.0f}".format(item) + "\n{0:.2%}".format(item / cm.flatten().sum())]
            for item in cm.flatten()
        ]
    ).reshape(2, 2)

    # Plotting confusion matrix heatmap
    plt.figure(figsize=(6, 4))
    sns.heatmap(cm, annot=labels, fmt="")
    plt.ylabel("True label")
    plt.xlabel("Predicted label")

