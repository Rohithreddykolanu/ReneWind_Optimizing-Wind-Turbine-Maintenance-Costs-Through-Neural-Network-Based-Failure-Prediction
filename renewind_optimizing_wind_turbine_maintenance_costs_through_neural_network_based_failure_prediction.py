# Define a function to plot a training metric (e.g., loss or accuracy) over epochs for both training and validation sets
def plot(history, metric):
  """
  Function to plot loss/accuracy

  history: an object which stores the metrics and losses.
  metric: can be one of Loss or Accuracy
  """

  # Create the figure and plot the training and validation values of the metric across epochs
  fig, ax = plt.subplots()
